package lesson1;

public class printPrintnl {
    public static void main(String[] args) {
//  метод println - вывод параметра на экран с переходом курсора на новую строку
        System.out.println("Hello mutterFuck!");
//  метод print - вывод параметра как он есть
        System.out.println("Hello");

//  пример: заисать буквы парами в столбик
        System.out.print("A ");
        System.out.println(" a");
        System.out.print("B ");
        System.out.println(" b");
        System.out.print("C ");
        System.out.println(" c");

//  пример: пустые строки между выводом значений
        System.out.println("abcd");
        System.out.println();
        System.out.println("efgh");


    }

}
